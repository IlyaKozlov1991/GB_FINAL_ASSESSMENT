package com.application.animalsapp;

public class Animal {

    String Name;
    String Birth;
    String Commands;

    public Animal() {}

    public void setName(String name) {
        Name = name;
    }

    public void setBirth(String birth) {
        Birth = birth;
    }

    public void setCommands(String commands) {
        Commands = commands;
    }
}
